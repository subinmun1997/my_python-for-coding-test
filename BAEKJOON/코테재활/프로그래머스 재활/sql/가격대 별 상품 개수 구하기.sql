SELECT TRUNCATE(PRICE, -4) AS PRICE_GROUP, COUNT(PRODUCT_ID) AS PRODUCTS
FROM PRODUCT
GROUP BY PRICE_GROUP
ORDER BY PRICE_GROUP ASC;

'''
<Mysql 반올림과 버림 함수>

1. ROUND(): 반올림을 위한 함수

ROUND(숫자, 반올림 할 자리 수)

ex)
ROUND(1234.56789)
-- 1234
ROUND(1234.56789, 1)
-- 1234.6
ROUND(1234.56789, 4)
-- 1234.5679
ROUND(1234.56789, -1)
-- 1230

2. TRUNCATE(): 버림을 위한 함수, 버림 할 자리수를 반드시 명시해야됨

TRUNCATE(숫자, 버림 할 자리 수)

ex)
TRUNCATE(1234.56789, 1)
-- 1234.5
TRUNCATE(1234.56789, 4)
-- 1234.5678
TRUNCATE(1234.56789, -1)
-- 1230
TRUNCATE(1234.56789, -2)
-- 1200
'''